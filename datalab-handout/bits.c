/* 
 * fitsBits - return 1 if x can be represented as an 
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int fitsBits(int x, int n) {
	int m1 = (0x1 << 31)>> 31;//-1
	int x1 = x>>(n+m1); //first 33-n bits
  return (!(x1^m1)|!(x1^0x0))|(n>>5); //all 0 or all 1 or n==32
}
/* 
 * divpwr2 - Compute x/(2^n), for 0 <= n <= 30
 *  Round toward zero
 *   Examples: divpwr2(15,1) = 7, divpwr2(-33,4) = -2
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int divpwr2(int x, int n) {
	int m1 = (0x1 << 31)>> 31;//-1
	int f = x>>31&0x1; //sign flag
	int m2 = ~(m1<<n);//mask
	int j = (!!(m2&x))&f;//negative and has remaider
    return (x>>n)+j;
}
