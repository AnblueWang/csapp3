/* 
 * isLessOrEqual - if x <= y  then return 1, else return 0 
 *   Example: isLessOrEqual(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLessOrEqual(int x, int y) {
	int f1 = (x>>31)&0x1; //x sign flag
	int f2 = (y>>31)&0x1; //y sign flag
	int f3 = f1^f2;//x y have same sign
	int f4 = (!!((x+(~y+1))&(0x1<<31)))|(!(x^y)); //x-y flag is zero or x==y
  return (f4&(!f3))|(f3&f1);
}
/*
 * ilog2 - return floor(log base 2 of x), where x > 0
 *   Example: ilog2(16) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 90
 *   Rating: 4
 */
int ilog2(int x) {
	int m1 = (1 << 31) >> 15;//0xffff0000
	int m2 = (0xff<<24)+(0xff<<8);//0xff00ff00
	int m3 = (0xf0<<24)+(0xf0<<16)+(0xf0<<8)+0xf0;//0xf0f0f0f0
	int m4 = (0xcc<<24)+(0xcc<<16)+(0xcc<<8)+0xcc;//0xcccccccc
	int m5 = (0xaa<<24)+(0xaa<<16)+(0xaa<<8)+0xaa;//0xaaaaaaaa
	int f1 = !!(m1&x);//first 16 bits is not 0
	int f2 = !!(m2&x);//first 8
	int f3 = !!(m3&x);
	int f4 = !!(m4&x);
	int f5 = !!(m5&x);
    return (f1<<4)+(f2<<3)+(f3<<2)+(f4<<1)+f5;
}
